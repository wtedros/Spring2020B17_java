package Day03_VariablesContinue;

public class ArithmaticOperations {

    public static void main(String[] args) {
        // +, -, *, /,  %

        System.out.println(5+3); // addition, equals 8
        System.out.println(10-3); // subtraction, equals 7
        System.out.println(10*2); // multiplication, equals 20
        System.out.println(10/2); // division, equals 5

        // System.out.println(9/0); you CANNOT have a 0 denominator

        System.out.println(10 % 3); // 1
        System.out.println(100 % 33);  // 1
    }
}
